package com.pateluday07.linkedlist.singly;

public class SinglyNode {

    private int val;
    private SinglyNode next;

    public SinglyNode(int val) {
        this.val = val;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public SinglyNode getNext() {
        return next;
    }

    public void setNext(SinglyNode next) {
        this.next = next;
    }


}
